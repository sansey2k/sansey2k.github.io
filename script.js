const flagMap = {
    en: "🇬🇧",
    de: "🇩🇪",
    ru: "🇷🇺",
    pl: "🇵🇱"
};

function setLanguage(lang) {
    // Update text content
    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        if (translations[lang]) {
            const value = translations[lang][key];
            if (Array.isArray(value)) {
                // For list items (experience results)
                element.innerHTML = value.map(item => `<li>${item}</li>`).join('');
            } else if (value) {
                element.textContent = value;
            }
        }
    });

    // Update dropdown button flag
    const dropdownBtn = document.getElementById('langDropdownBtn');
    const flagSpan = dropdownBtn.querySelector('.flag');
    flagSpan.textContent = flagMap[lang];

    document.documentElement.lang = lang;
    localStorage.setItem('preferredLanguage', lang);
}

// Dropdown toggle
const dropdownBtn = document.getElementById('langDropdownBtn');
const dropdownContent = document.getElementById('langDropdown');

dropdownBtn.addEventListener('click', (e) => {
    e.stopPropagation();
    dropdownContent.classList.toggle('show');
});

// Close dropdown when clicking outside
document.addEventListener('click', () => {
    dropdownContent.classList.remove('show');
});

// Language selection
document.querySelectorAll('.lang-option').forEach(btn => {
    btn.addEventListener('click', (e) => {
        e.stopPropagation();
        const lang = btn.getAttribute('data-lang');
        setLanguage(lang);
        dropdownContent.classList.remove('show');
    });
});

// Initialize with saved language or default to English
const savedLang = localStorage.getItem('preferredLanguage') || 'en';
setLanguage(savedLang);
